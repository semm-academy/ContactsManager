package com.company;

public class Contact {


        String fullName;
        String email;
        String phoneNumber;
        Contact() {

            fullName=fullName;
            email = email;
            phoneNumber=phoneNumber;
        }

        public Contact(String name, String email, String phoneNumber){
            this.fullName = name;
            this.email = email;
            this.phoneNumber = phoneNumber;
        }



}


